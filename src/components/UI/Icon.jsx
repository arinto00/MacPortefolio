// Generic Icon wrapper component
const Icon = ({
  name,
  size = 24,
  color = 'currentColor',
  className = '',
  ...props
}) => {
  const iconMap = {
    user: UserIcon,
    folder: FolderIcon,
    file: FileIcon,
    mail: MailIcon,
    calculator: CalculatorIcon,
    trash: TrashIcon,
  }

  const IconComponent = iconMap[name]

  if (!IconComponent) {
    console.warn(`Icon "${name}" not found`)
    return null
  }

  return (
    <IconComponent size={size} color={color} className={className} {...props} />
  )
}

export default Icon
